void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	s;
	int	temp;

	i = 0;
	s = size - 1;
	while (i < s)
	{
		temp = tab[i];
		tab[i] = tab[s];
		tab[s] = temp;
		i++;
		s--;
	}
}
